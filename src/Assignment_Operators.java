public class Assignment_Operators {

    public static void main(String args[]){

        int itemsAvailable = 7;

        System.out.println("Current available items:  " + itemsAvailable);

        itemsAvailable += 10;

        System.out.println("Available items updated:  " + itemsAvailable);
        //Subtracting 9 items

        itemsAvailable -= 9;
        System.out.println("Available items after subtraction:  " + itemsAvailable);

    }
}
